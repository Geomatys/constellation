    public JCswFsEditPane(final Automatic configuration) {
        initComponents();
        
        if (configuration != null) {
            if (configuration.getDataDirectoryValue() != null) {
                guiDataDir.setText(configuration.getDataDirectoryValue());
            }
            if (configuration.getProfileValue() != null) {
                guiProfileCombo.setSelectedItem(configuration.getProfileValue());
            }
        }
    }
    
    @Override
    public Automatic getConfiguration() {
        final Automatic configuration = new Automatic("filesystem", guiDataDir.getText());
        configuration.setProfile((String)guiProfileCombo.getSelectedItem());
        return configuration;
    }

